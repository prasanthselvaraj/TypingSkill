package com.pgx.java.web;

public class HeadlineStatement {
	private String title;

	private String para;
	
	private String dialogSentence;
	
	
	
	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getPara() {
		return para;
	}

	public void setPara(String para) {
		this.para = para;
	}
	
	public String getDialogSentence() {
		return dialogSentence;
	}

	public void setDialogSentence(String dialogSentence) {
		this.dialogSentence = dialogSentence;
	}
}
